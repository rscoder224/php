<!-- Increment and Decrement oprator -->
<?php

//  Increment and Decrement oprators

//         Example                  Name                          Effect
//          ++$a                    Pre-increment                 Increment $a by one than return $a
//          $a++                    Post-increment                Return $a than increment $a by one;  
//          --$a                    Pre-decrement                 decrement $a by one than return $a ;
//           $a--                    Post-decrement                Return $a than decrement $a by one;



$a = 2;

echo ++$a;
echo "<br>";

$b = $a++;
echo $b;
echo "<br>";

echo --$b;
echo "<br>";
$c = $b--;
echo $c;
echo "<br>";

